    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
            String id = idField.getText().trim();
            String wetMarketName = wetMarketNameField.getText().trim();
            String location = locationField.getText().trim();
            String testedAnimal = testedAnimalField.getText().trim();
            String covidResult = buttonGroup1.getSelection().getActionCommand();
            String date = dateChooserCombo1.getText().trim();

            if (id.isEmpty() || wetMarketName.isEmpty() || location.isEmpty() || testedAnimal.isEmpty() || covidResult.isEmpty() || date.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Fill in the form properly!", "Error", JOptionPane.ERROR_MESSAGE);
            } else {

                WetMarketTest wetMarketTest = new WetMarketTest(id, wetMarketName, location, testedAnimal, covidResult, date);
                testList.add(wetMarketTest);

                DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
                String[] data = {id, wetMarketName, location, testedAnimal, covidResult, date};
                tableModel.addRow(data);

                JOptionPane.showMessageDialog(null, "You have succesfully added the information", "Info", JOptionPane.INFORMATION_MESSAGE);
                saveToFile();
            }
        } catch (IOException e) {
            e.printStackTrace();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Fill in the form properly!", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_addBtnActionPerformed

    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        int index = jTable1.getSelectedRow();
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRowCount() == 1) {

            testList.remove(index);
            tableModel.removeRow(index);
            JOptionPane.showMessageDialog(null, "You have succesfully removed the information", "Info", JOptionPane.INFORMATION_MESSAGE);
            try {
                saveToFile();
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Select a row !", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_removeBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        int index = jTable1.getSelectedRow();
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        UpdateTestForm updateTestForm = new UpdateTestForm();
        if (jTable1.getSelectedRowCount() == 1) {
            System.out.println("hi");

            updateTestForm.setUpdateInfoTestListener(new UpdateTestListener() {
                @Override
                public void updateEventOccured(UpdateTestInfo updateInfo) {
                    String id = updateInfo.getId();
                    String wetMarketName = updateInfo.getWetMarketName();
                    String location = updateInfo.getLocation();
                    String testedAnimal = updateInfo.getTestedAnimal();
                    String covidResult = updateInfo.getCovidResult();
                    String date = updateInfo.getDate();

                    WetMarketTest updatedWetMarketTest = testList.get(index);
                    updatedWetMarketTest.setId(id);
                    updatedWetMarketTest.setWetMarketName(wetMarketName);
                    updatedWetMarketTest.setLocation(location);
                    updatedWetMarketTest.setTestedAnimal(testedAnimal);
                    updatedWetMarketTest.setCovidResult(covidResult);
                    updatedWetMarketTest.setDate(date);

                    tableModel.setValueAt(id, jTable1.getSelectedRow(), 0);
                    tableModel.setValueAt(wetMarketName, jTable1.getSelectedRow(), 1);
                    tableModel.setValueAt(location, jTable1.getSelectedRow(), 2);
                    tableModel.setValueAt(testedAnimal, jTable1.getSelectedRow(), 3);
                    tableModel.setValueAt(covidResult, jTable1.getSelectedRow(), 4);
                    tableModel.setValueAt(date, jTable1.getSelectedRow(), 5);

                }
            });
            updateTestForm.setVisible(true);

            try {
                saveToFile();
            } catch (IOException ex) {
                Logger.getLogger(WetMarketTestPanel.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Select a row !", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void barChartBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barChartBtnActionPerformed
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        //HashMap<String,
        String[] name = new String[testList.size()];
        int countPosi = 0;
        for (int i = 0; i < name.length; i++) {
            WetMarketTest wmt = testList.get(i);
            name[i] = wmt.getWetMarketName();
        }

        HashMap<String, WetMarketTest> wetHashMap = new HashMap<>();
        for (WetMarketTest wmt : testList) {
            wetHashMap.put(wmt.getWetMarketName(), wmt);
        }

        Set keys = wetHashMap.keySet();
        Iterator it = keys.iterator();
        while (it.hasNext()) {
            String wetMarketName = (String) it.next();
            System.out.println("name " + wetMarketName);
            int positiveCounter = 0;
            int negativeCounter = 0;
            for (WetMarketTest w : testList) {
                if (w.getWetMarketName().equalsIgnoreCase(wetMarketName)) {
                    if (w.getCovidResult().equalsIgnoreCase("POSITIVE")) {
                        positiveCounter++;
                    } else {
                        negativeCounter++;
                    }
                }

            }
            dataset.setValue(positiveCounter, "POSITIVE", wetMarketName);
            dataset.setValue(negativeCounter, "NEGATIVE", wetMarketName);

        }
//        dataset.setValue(30, "POSITIVE", "PASAR MALAM");
//        dataset.setValue(2, "NEGATIVE", "PASAR MALAM");
//
//        dataset.setValue(30, "POSITIVE", "PASAR JOKO");
//        dataset.setValue(2, "NEGATIVE", "PASAR JOKO");

        JFreeChart jchart = ChartFactory.createBarChart("COVID-19 Test on Animals in Wet Markets", "WET MARKETS", "COVID-19 CASES", dataset, PlotOrientation.VERTICAL, true, true, false);

        ChartFrame chartFrame = new ChartFrame("COVID-19 on Animal in Wet Market", jchart);

        chartFrame.setVisible(true);
        chartFrame.setSize(500, 500);

    }//GEN-LAST:event_barChartBtnActionPerformed

