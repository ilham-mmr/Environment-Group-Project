    private void showData() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        for (WetMarketTest wmt : testList) {
            String[] data = {wmt.getId(), wmt.getWetMarketName(), wmt.getLocation(), wmt.getTestedAnimal(), wmt.getCovidResult(), wmt.getDate()};
            tableModel.addRow(data);
        }
    }

    public void saveToFile() throws IOException {
        FileOutputStream fos = new FileOutputStream(new File("covid-19_WetMarket.txt"));
        ObjectOutputStream oos = new ObjectOutputStream(fos);

        WetMarketTest[] marketTests = testList.toArray(new WetMarketTest[testList.size()]);

        oos.writeObject(marketTests);

        oos.close();

    }

    public void loadFromFile() throws IOException {
        FileInputStream fis = new FileInputStream(new File("covid-19_WetMarket.txt"));
        ObjectInputStream ois = new ObjectInputStream(fis);

        try {
            WetMarketTest[] wetMarketTests = (WetMarketTest[]) ois.readObject();
            testList.clear();
            testList.addAll(Arrays.asList(wetMarketTests));
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }

        ois.close();

    }

